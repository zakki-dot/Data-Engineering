    csv_reader = csv.DictReader(csvfile)

    for row in csv_reader:
        eid = row["eid"].strip()  # Employee ID (eid)
        title = row["title"].strip()  # Employee title
        manager_eid = row["reports_to"].strip()  # Manager's employee ID (reports_to)

        # If the employee has a manager and the manager's title matches the employee's title
        if manager_eid in employee_titles:
            manager_title = employee_titles[manager_eid]  # Get the manager's title

            if title == manager_title:
                invalid_title_records += 1  # Increment counter if titles are the same

