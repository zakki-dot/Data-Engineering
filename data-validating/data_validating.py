# Initialize a list to store the salaries
salaries = []

# Step 1: Read the CSV file and collect salaries
with open(file_name, 'r') as csvfile:
    csv_reader = csv.DictReader(csvfile)

    for row in csv_reader:
        salary = row["salary"].strip()  # Get the salary for each employee
        try:
            salary = float(salary)  # Convert the salary to a float
            salaries.append(salary)  # Add the salary to the list
        except ValueError:
            continue  # Skip any rows with invalid salary values

# Step 2: Plot the histogram of salaries
plt.figure(figsize=(10, 6))
plt.hist(salaries, bins=30, color='skyblue', edgecolor='black')
plt.title('Histogram of Employee Salaries')
plt.xlabel('Salary')
plt.ylabel('Frequency')
plt.grid(True)
plt.show()

# Step 3:normality test (Shapiro-Wilk test)
stat, p_value = shapiro(salaries)

# Step 4: Interpret the result
print(f"Shapiro-Wilk Test Statistic: {stat}")
print(f"P-value: {p_value}")
if p_value > 0.05:
    print("The salaries are likely normally distributed (fail to reject H0).")
else:
    print("The salaries are not normally distributed (reject H0).")


