# Initialize a set to track employee ids and a counter for duplicates
employee_ids = set()
duplicate_eids = 0

# Step 1: Read the file and check for duplicate employee ids
with open(file_name, 'r') as csvfile:
    csv_reader = csv.DictReader(csvfile)

    for row in csv_reader:
        eid = row["eid"].strip()  # Get the employee ID (eid)

        # If the eid is already in the set then it's a duplicate
        if eid in employee_ids:
            duplicate_eids += 1  # Increment the counter for duplicate eids
        else:
            employee_ids.add(eid)  # Add the eid to the set if it's not a duplicate

# Step 2: Print the result
if duplicate_eids > 0:
    print(f"Dataset is not valid. {duplicate_eids} duplicate employee id(s) found.")
else:
    print("Dataset is valid. Each employee has a unique employee id.")


